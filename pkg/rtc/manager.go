// handleIncomingOffer handles an incoming RTC offer
func (cm *CallManager) handleIncomingOffer(callID string, remoteIdentity []byte, callType string, sdp string) {
	// Notify callback
	if cm.onIncomingCall != nil {
		cm.onIncomingCall(callID, remoteIdentity, callType, sdp)
	}

	// Auto-answer if configured
	if cm.config.AutoAnswer {
		go func() {
			time.Sleep(cm.config.AutoAnswerTimeout)
			if err := cm.AnswerIncomingCall(callID); err != nil {
				logger.Errorw("failed to auto-answer call", "call", callID, "error", err)
			}
		}()
	}
}

