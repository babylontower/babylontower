// CallManager provides high-level call lifecycle management
type CallManager struct {
	identity   *identity.Identity
	messaging  *messaging.Service
	storage    storage.Storage
	sessionMgr *SessionManager
	signaling  *SignalingService

	ctx    context.Context
	cancel context.CancelFunc
	wg     sync.WaitGroup

	mu sync.RWMutex

	// Current call state
	currentCallID string

	// Configuration
	config *CallConfig

	// Event callbacks
	onCallStarted   func(callID string, remoteIdentity []byte, callType string)
	onCallConnected func(callID string)
	onCallEnded     func(callID string, reason string, duration time.Duration)
	onIncomingCall  func(callID string, remoteIdentity []byte, callType string, sdp string)
	onCallError     func(callID string, err error)
}

// CallConfig holds configuration for call management
type CallConfig struct {
	// AutoAnswer determines whether to automatically answer incoming calls
	AutoAnswer bool
	// AutoAnswerTimeout is the delay before auto-answering
	AutoAnswerTimeout time.Duration
	// CallTimeout is the maximum duration for a call (0 = no limit)
	CallTimeout time.Duration
	// EnableVideo enables video calls (false = audio only)
	EnableVideo bool
	// MaxICECandidates is the maximum number of ICE candidates to gather
	MaxICECandidates int
	// ICEGatherTimeout is the timeout for ICE candidate gathering
	ICEGatherTimeout time.Duration
}

// DefaultCallConfig returns the default call configuration
func DefaultCallConfig() *CallConfig {
	return &CallConfig{
		AutoAnswer:        false,
		AutoAnswerTimeout: 0,
		CallTimeout:       0, // No limit
		EnableVideo:       true,
		MaxICECandidates:  20,
		ICEGatherTimeout:  ICEGatherTimeout,
	}
}

// NewCallManager creates a new call manager
func NewCallManager(
	id *identity.Identity,
	msgService *messaging.Service,
	store storage.Storage,
	config *CallConfig,
) (*CallManager, error) {
	if config == nil {
		config = DefaultCallConfig()
	}

	ctx, cancel := context.WithCancel(context.Background())

	// Create session manager
	sessionMgr := NewSessionManager(store, id)

	// Create signaling service
	signaling := NewSignalingService(id, msgService, sessionMgr)

	cm := &CallManager{
		identity:   id,
		messaging:  msgService,
		storage:    store,
		sessionMgr: sessionMgr,
		signaling:  signaling,
		ctx:        ctx,
		cancel:     cancel,
		config:     config,
	}

	// Set up signaling callbacks
	cm.setupSignalingCallbacks()

	return cm, nil
}

// setupSignalingCallbacks configures the signaling service callbacks
func (cm *CallManager) setupSignalingCallbacks() {
	cm.signaling.SetOnIncomingOffer(cm.handleIncomingOffer)
	cm.signaling.SetOnIncomingAnswer(cm.handleIncomingAnswer)
	cm.signaling.SetOnIncomingICE(cm.handleIncomingICE)
	cm.signaling.SetOnIncomingHangup(cm.handleIncomingHangup)

	cm.sessionMgr.SetOnStateChanged(cm.handleStateChanged)
	cm.sessionMgr.SetOnCallEnded(cm.handleSessionEnded)
}

// Start begins the call manager
func (cm *CallManager) Start() error {
	cm.mu.Lock()
	defer cm.mu.Unlock()

	if err := cm.signaling.Start(); err != nil {
		return fmt.Errorf("failed to start signaling service: %w", err)
	}

	logger.Info("Call manager started")
	return nil
}

// Stop gracefully stops the call manager and ends all active calls
func (cm *CallManager) Stop() {
	cm.mu.Lock()
	defer cm.mu.Unlock()

	// End all active calls
	sessions := cm.sessionMgr.GetAllActiveSessions()
	for _, session := range sessions {
		_ = cm.signaling.SendHangup(session.CallID, HangupNormal)
		_ = cm.sessionMgr.EndCall(session.CallID, HangupNormal)
	}

	cm.signaling.Stop()
	cm.sessionMgr.Stop()
	cm.cancel()
	cm.wg.Wait()

	logger.Info("Call manager stopped")
}

// StartOutgoingCall initiates an outgoing call to a contact
func (cm *CallManager) StartOutgoingCall(remoteIdentity []byte, callType string) (string, error) {
	cm.mu.Lock()
	defer cm.mu.Unlock()

	// Check if we're already in a call
	if cm.currentCallID != "" {
		return "", fmt.Errorf("already in call: %s", cm.currentCallID)
	}

	// Validate call type
	if callType == CallTypeVideo && !cm.config.EnableVideo {
		callType = CallTypeAudio
	}

	// Generate SDP offer (placeholder - would be generated by WebRTC)
	sdpOffer := cm.generateSDPOffer(callType)

	// Send offer via signaling
	session, err := cm.signaling.SendOffer(remoteIdentity, callType, sdpOffer)
	if err != nil {
		return "", fmt.Errorf("failed to send offer: %w", err)
	}

	cm.currentCallID = session.CallID

	logger.Infow("started outgoing call", "type", callType, "remote", hex.EncodeToString(remoteIdentity)[:16], "call", session.CallID)

	// Notify callback
	if cm.onCallStarted != nil {
		cm.onCallStarted(session.CallID, remoteIdentity, callType)
	}

	// Start call timeout if configured
	if cm.config.CallTimeout > 0 {
		go cm.startCallTimeout(session.CallID, cm.config.CallTimeout)
	}

	return session.CallID, nil
}

// AnswerIncomingCall answers an incoming call
func (cm *CallManager) AnswerIncomingCall(callID string) error {
	cm.mu.Lock()
	defer cm.mu.Unlock()

	if cm.currentCallID != "" {
		return fmt.Errorf("already in call: %s", cm.currentCallID)
	}

	session, err := cm.sessionMgr.GetSession(callID)
	if err != nil {
		return ErrCallNotFound
	}

	// Update state
	if err := cm.sessionMgr.UpdateState(callID, CallStateAccepted); err != nil {
		return fmt.Errorf("failed to update call state: %w", err)
	}

	// Generate SDP answer (placeholder)
	sdpAnswer := cm.generateSDPAnswer(session.CallType, session.RemoteSDP)

	// Send answer
	if err := cm.signaling.SendAnswer(callID, sdpAnswer); err != nil {
		return fmt.Errorf("failed to send answer: %w", err)
	}

	cm.currentCallID = callID

	logger.Infow("answered incoming call", "call", callID)

	// Notify callback
	if cm.onCallStarted != nil {
		cm.onCallStarted(callID, session.RemoteIdentity, session.CallType)
	}

	return nil
}

// RejectIncomingCall rejects an incoming call
func (cm *CallManager) RejectIncomingCall(callID string, reason string) error {
	_, err := cm.sessionMgr.GetSession(callID)
	if err != nil {
		return ErrCallNotFound
	}

	// Send hangup
	if err := cm.signaling.SendHangup(callID, reason); err != nil {
		logger.Warnw("failed to send hangup", "error", err)
	}

	// End call locally
	if err := cm.sessionMgr.EndCall(callID, reason); err != nil {
		return fmt.Errorf("failed to end call: %w", err)
	}

	logger.Infow("rejected incoming call", "call", callID, "reason", reason)

	return nil
}

// EndCall ends the current call
func (cm *CallManager) EndCall(reason string) error {
	cm.mu.Lock()
	callID := cm.currentCallID
	cm.mu.Unlock()

	if callID == "" {
		return fmt.Errorf("no active call")
	}

	return cm.EndCallByID(callID, reason)
}

// EndCallByID ends a specific call by ID
func (cm *CallManager) EndCallByID(callID string, reason string) error {
	// Send hangup
	if err := cm.signaling.SendHangup(callID, reason); err != nil {
		logger.Warnw("failed to send hangup", "error", err)
	}

	// End call locally
	if err := cm.sessionMgr.EndCall(callID, reason); err != nil {
		return fmt.Errorf("failed to end call: %w", err)
	}

	cm.mu.Lock()
	if cm.currentCallID == callID {
		cm.currentCallID = ""
	}
	cm.mu.Unlock()

	logger.Infow("ended call", "call", callID, "reason", reason)

	return nil
}

// SendICECandidate sends an ICE candidate for the current call
func (cm *CallManager) SendICECandidate(candidate string, sdpMid string, mlineIdx uint32) error {
	cm.mu.RLock()
	callID := cm.currentCallID
	cm.mu.RUnlock()

	if callID == "" {
		return fmt.Errorf("no active call")
	}

	return cm.signaling.SendICECandidate(callID, candidate, sdpMid, mlineIdx)
}

// GetActiveCall returns the current active call session
func (cm *CallManager) GetActiveCall() (*CallSession, error) {
	cm.mu.RLock()
	callID := cm.currentCallID
	cm.mu.RUnlock()

	if callID == "" {
		return nil, fmt.Errorf("no active call")
	}

	return cm.sessionMgr.GetSession(callID)
}

// GetCall returns a call session by ID
func (cm *CallManager) GetCall(callID string) (*CallSession, error) {
	return cm.sessionMgr.GetSession(callID)
}

// GetAllCalls returns all call sessions (active and ended)
func (cm *CallManager) GetAllCalls() []*CallSession {
	return cm.sessionMgr.GetAllActiveSessions()
}

// IsInCall returns true if currently in a call
func (cm *CallManager) IsInCall() bool {
	cm.mu.RLock()
	defer cm.mu.RUnlock()
	return cm.currentCallID != ""
}

